public class Student {     
    public String name;     
    public int mark; 
    public void setName(String n){        
        this.name=n; 
    } 
    
    public String getName(){        
        return name; 
    } 

    public void setMark(int m){     
        this.mark=m;     
    }     
    
    public int getMark(){        
        return mark; 
    } 
} 
